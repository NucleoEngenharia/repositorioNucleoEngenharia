package com.nucleo.endereco.TO;

public class CidadeTO {
	private int id;
	private String nome;
	private String uf;
	public int getId() {
		return id;
	}
	public String getNome() {
		return nome;
	}
	public String getUf() {
		return uf;
	}
	public void setId(int id) {
		this.id = id;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public void setUf(String uf) {
		this.uf = uf;
	}
	

}
